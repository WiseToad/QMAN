CREATE TABLE LOG_FILTERS (
    APP_CODE    VARCHAR2(50) NOT NULL,
    MSG_LEVEL   VARCHAR2(15) NOT NULL,
    FILTER_TYPE VARCHAR2(10) NOT NULL,

    CHECK (filter_type IN ('INCLUDE', 'EXCLUDE'))
);

COMMENT ON TABLE LOG_FILTERS IS 'Фильтры сообщений';
COMMENT ON COLUMN LOG_FILTERS.APP_CODE IS 'Код приложения';
COMMENT ON COLUMN LOG_FILTERS.FILTER_TYPE IS 'Тип фильтрации: включить/исключить';
COMMENT ON COLUMN LOG_FILTERS.MSG_LEVEL IS 'Маска уровня важности сообщения';

CREATE BITMAP INDEX LOG_FILTERS_BM01 ON LOG_FILTERS (APP_CODE, FILTER_TYPE);
